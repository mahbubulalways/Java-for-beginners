package javaforbehinner;

public class MathClassInJava {
    public static void main(String[] args) {
        int a = Math.abs(-100); // * abs return positive value
        System.out.println(a);
        double b = Math.sqrt(25);
        System.out.println(b);
        double c = Math.pow(2, 3);
        System.out.println(c);
        double d = Math.PI;
        System.out.println(d);
        double e = Math.log(1);
        System.out.println(e);
        int f = Math.max(12, 155);
        System.out.println(f);
        double g = Math.exp(12);
        System.out.println(g);
        double h = Math.ceil(122.7);
        System.out.println(h);
        double i = Math.round(12.3);
        System.out.println(i);
        double j = Math.floor(12.2);
        System.out.println(j);

    }
}
