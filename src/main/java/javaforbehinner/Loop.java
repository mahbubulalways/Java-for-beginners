public class Loop {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("People are change any time. Be Strong.");
        }
        int i = 0;
        while (i < 10) {
            System.out.println("Its While LOOP");
            i++;
        }
        
        int a = 10;
        do {            
            System.out.println(a);
            a--;
        } while (a > 0);
        
    }
}
